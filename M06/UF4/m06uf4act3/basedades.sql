CREATE TABLE VENDES (
 ID    		SMALLINT(4)  NOT NULL PRIMARY KEY,
 IDPRODUCTE	SMALLINT(4) NOT NULL REFERENCES producte(ID),
 DATAVENDA	DATE,
 QUANTITAT	TINYINT(3)
);

CREATE TABLE COMANDES (
 ID    		SMALLINT(4)  NOT NULL PRIMARY KEY,
 IDPRODUCTE	SMALLINT(4) NOT NULL REFERENCES producte(ID),
 DATACOMANDA	DATE,
 QUANTITAT	TINYINT(3)
);
