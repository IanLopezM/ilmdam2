
CREATE TABLE PRODUCTE (
 ID		SMALLINT(4)  NOT NULL PRIMARY KEY,
 DESCRIPCIO	VARCHAR(50),
 STOCKACTUAL	TINYINT(3),
 STOCKMINIM	TINYINT(3),
 PVP		FLOAT(6,2)
);

